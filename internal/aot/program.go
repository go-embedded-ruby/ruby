package aot

import (
	"fmt"
	"strconv"
	"strings"

	"github.com/go-embedded-ruby/ruby/internal/bytecode"
)

// CompileProgram lowers the top-level methods of a program to a single Go source
// file (package vm) that `rbgo build` links into the binary: one function per
// compilable method plus an init() registering each with the dispatch seam
// (RegisterCompiled). Methods this stage cannot lower — and any redefinition of
// a name already taken — are skipped and left to the interpreter.
//
// It handles methods defined at the top level (owner Object); methods inside
// class/module bodies are not walked yet and remain interpreted. ok is false
// when nothing could be compiled, so the caller can build a plain binary.
func CompileProgram(top *bytecode.ISeq) (content string, keys []string, ok bool) {
	var funcs strings.Builder
	var regs strings.Builder
	seen := map[string]bool{}

	for _, in := range top.Insns {
		if in.Op != bytecode.OpDefineMethod {
			continue
		}
		name := top.Names[in.A]
		if seen[name] {
			continue // only the first definition of a name is compiled (matches deopt-on-redef)
		}
		child := top.Children[in.B]
		goName := "aotm" + strconv.Itoa(len(keys))
		src, lowered := Compile(child, goName, name)
		if !lowered {
			continue
		}
		seen[name] = true
		funcs.WriteString(src)
		funcs.WriteString("\n")
		key := "Object#" + name
		fmt.Fprintf(&regs, "\tRegisterCompiled(%q, (*VM).%s)\n", key, goName)
		keys = append(keys, key)
	}

	if len(keys) == 0 {
		return "", nil, false
	}

	var b strings.Builder
	b.WriteString("// Code generated by `rbgo build`; DO NOT EDIT.\n")
	b.WriteString("package vm\n\n")
	// object is always referenced (every signature is object.Value); bytecode only
	// when a lowered body emits an operator through vm.binaryOp.
	body := funcs.String()
	b.WriteString("import (\n")
	if strings.Contains(body, "bytecode.") {
		b.WriteString("\t\"github.com/go-embedded-ruby/ruby/internal/bytecode\"\n")
	}
	b.WriteString("\t\"github.com/go-embedded-ruby/ruby/internal/object\"\n")
	b.WriteString(")\n\n")
	b.WriteString(body)
	b.WriteString("func init() {\n")
	b.WriteString(regs.String())
	b.WriteString("}\n")
	return b.String(), keys, true
}
